% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
h = str2num ( get ( handles.edit1 , 'string') );
c = str2num ( get ( handles.edit2 , 'string') )/1000;
fstop = str2num ( get ( handles.edit3,'string') );
f = str2num ( get ( handles.edit4,'string') )/1000;
hn = h / ( 1+((h-c)*c*fstop)/(f^2));
hf = h / ( 1-((h-c)*c*fstop)/(f^2));
dof = hf - hn;
HN = num2str ( hn );
HF = num2str ( hf );
set ( handles.text6,'string',HN );
set ( handles.text8,'string',HF);
DOF = num2str(dof);
set ( handles.text10,'string',DOF);
